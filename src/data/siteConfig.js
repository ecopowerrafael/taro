export function buildAbsoluteUrl(pathname = '/') {
  const normalizedPath = pathname.startsWith('/') ? pathname : `/${pathname}`
  return `${siteConfig.siteUrl}${normalizedPath}`
}

export function buildAbsoluteImageUrl(imagePath = siteConfig.defaultImage) {
  if (!imagePath) {
    return buildAbsoluteUrl(siteConfig.defaultImage)
  }

  if (imagePath.startsWith('data:') || imagePath.startsWith('blob:') || imagePath.startsWith('javascript:')) {
    return buildAbsoluteUrl(siteConfig.defaultImage)
  }

  if (imagePath.startsWith('http://') || imagePath.startsWith('https://')) {
    return imagePath
  }
  return buildAbsoluteUrl(imagePath)
}

export function getRouteSeo(pathname) {
  const exactMatch = exactRouteSeo[pathname]
  if (exactMatch) {
    return exactMatch
  }

  const prefixMatch = prefixRouteSeo.find((route) => pathname.startsWith(route.prefix))
  if (prefixMatch) {
    return prefixMatch.data
  }

  return {
    title: siteConfig.defaultTitle,
    description: siteConfig.defaultDescription,
    keywords: siteConfig.defaultKeywords,
  }
}